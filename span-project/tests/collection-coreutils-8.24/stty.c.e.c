static void
wrapf (const char *message,...)
{
  va_list args;
  char *buf;
  int buflen;

  __builtin_va_start(args, message);
  buflen = rpl_vasprintf (&buf, message, args);
  __builtin_va_end(args);

  if (buflen < 0)
    xalloc_die ();

  if (0 < current_col)
    {
      if (max_col - current_col < buflen)
        {
          putchar_unlocked ('\n');
          current_col = 0;
        }
      else
        {
          putchar_unlocked (' ');
          current_col++;
        }
    }

  fputs_unlocked (buf,stdout);
  free (buf);
  current_col += buflen;
}

